# 6º Exercício => Faça um sistema que converta dolare em reais:

# Apresentação do Sistema:
print("\nConversor de Dolares em Reais:\n")

# Entrada de valores:
a = int(input('Digite a cotação atual do dólar: '))
b = int(input('Digite uma quantidade em dolares: '))

# Processamento/Conta:
c = a * b
 
# Saída de valores:
print(f"\nEste valor representa {c} reais!\n")