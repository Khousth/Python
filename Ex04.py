# 4º Exercício => Faça um sistema que calcule a média aritmética de quatro valores.

# Apresentação do Sistema:
print("\nCalculadora de Média Aritmética de Quatro Valores:\n")

# Entrada de valores:
a = int(input('Digite o primeiro valor: '))
b = int(input('Digite o segundo valor: '))
c = int(input('Digite o terceiro valor: '))
d = int(input('Digite o quarto valor: '))

# Processamento/Conta:
v = (a + b + c + d) /4

# Saída de valores:
print(f"\nA Média Aritmética é {v}!\n")