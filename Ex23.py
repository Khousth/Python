# 23º Exercício => Insira três variáveis e digite se a última é maior que a soma das primeiras:

# Apresentação do Sistema:
print("\nSistema que diz se a última variável é maior que a soma das primeiras:\n")

# Entrada de Valores:
A = float(input("Digite o valor 1: "))
B = float(input("Digite o valor 2: "))
C = float(input("Digite o valor 3: "))

# Etapa de Decisão:
if ((A + B) > C):
    print("\nA soma é maior que o valor 3\n")
else:
    print("\nA soma é menor que o valor 3\n")
