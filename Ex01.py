# 1º Exercício => Faça um sistema que calcule a área de um retângulo:

# Apresentação do Sistema:
print("\nCalculadora de Área para Retângulos:\n")

# Entrada de valores:
a = int(input('Digite a base do retângulo: '))
b = int(input('Digite a altura do retângulo: '))

# Processamento/Conta:
c = a * b

# Saída de valores:
print(f"\nA área do Retângulo é {c}!\n")