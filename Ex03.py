# 3º Exercício => Faça um sistema que calcule a área de um triângulo:

# Apresentação do Sistema:
print("\nCalculadora de Área para Triângulos:\n")

# Entrada de valores:
a = int(input('Digite a base do triângulo: '))
b = int(input('Digite a altura do triângulo: '))

# Processamento/Conta:
c = (a * b) / 2

# Saída de valores:
print(f"\nA área do Triângulo Retângulo é {c}!\n")