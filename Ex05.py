# 5º Exercício => Faça um sistema que conversa celsius em fahrenheit:

# Apresentação do Sistema:
print("\nConversor de Celsius em Fahrenheit:\n")

# Entrada de valores:
a = int(input('Digite o valor em Celsius: '))

# Processamento/Conta:
c = (a * 9/5) + 32
 
# Saída de valores:
print(f"Este valor representa {c} Fahrenheits!\n")