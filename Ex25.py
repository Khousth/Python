# 25º Exercício => Sistema que diz se o valor é par ou impar:

# Apresentação do Sistema:
print("\nSistema que diz se o valor é par ou impar:\n")

# Entrada de Valores:
numero = int(input("Digite um número: "))

# Etapa de Decisão:
if (numero % 2 == 0):
    print("O número é par")
else:
    print("O Número é impar")
